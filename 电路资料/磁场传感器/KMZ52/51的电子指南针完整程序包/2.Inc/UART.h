#ifndef _UART_H_
#define _UART_H_
/****************************************************************************************************************************
**函数名称：InitCOM                                                                                                        **
**函数功能：串口初始化函数                                                                                                 **   
**入口参数：BpsLvl : 波特率等级。1=600bps,2=1200bps....5=9600bps,6=19200bps...8=57600bps                                   **
**出口参数：无                                                                                                             **
**具体资源：无                                                                                                             **
**调用程序：无                                                                                                             **
**备    注：在系统时钟为晶振为22.1184M　方式1 波特率600－57600                                                             **
****************************************************************************************************************************/
void InitCOM(void);

/****************************************************************************************************************************
**函数名称：SendUart                                                                                                       **
**函数功能：通过串口发送                                                                                                   **   
**入口参数：要发送的一字节数据                                                                                             **
**出口参数：无                                                                                                             **
**具体资源：无                                                                                                             **
**调用程序：无                                                                                                             **
**备    注：                                                                                                               **
****************************************************************************************************************************/
void UartSendByte(unsigned char dat);

/****************************************************************************************************************************
**函数名称：UartSendStr                                                                                                    **
**函数功能：通过串口发送一串字符                                                                                           **   
**入口参数：要发送的字符串                                                                                                 **
**出口参数：无                                                                                                             **
**具体资源：无                                                                                                             **
**调用程序：无                                                                                                             **
**备    注：                                                                                                               **
****************************************************************************************************************************/
void UartSendStr(unsigned char *pStr);

#endif